/* Outputs a drawing at the specified location */
void print_mode_shape(int n, int m, int x, int y, int c)
{
    int i = 0;
    int xx = x;
    int yy = y;

    for (i = 0; i < 16; i++)
    {
        if (i != 0 && i % 4 == 0)
        {
            yy += 1;
            xx = x;
        }

        if (shape[n][m][i] == 1) 
        {
            printf("\033[%d;%dH",yy,xx);
            printf("\033[%dm[]",c);
            printf("\033[0m");
        }

        xx += 2;
    }
}